    public async validate(): Promise<any> {
        return this.validateSchemas(this.contract);
    }

    private async validateSchemas(_contract: any = {}): Promise<any> {
        
        let errors: Array<any> = [];
        
        let contract = new Contract();
        contract.parentId = _contract.parentId;
        contract.parentType = _contract.parentType;
        contract.name = _contract.name;
        contract.signatureDate = _contract.signatureDate;
        contract.fillingDate = _contract.fillingDate;
        contract.file = _contract.file;
        let contractErrors = await validate(contract, { skipMissingProperties: true });
        errors = errors.concat(RemodelErrors(contractErrors, 'contract'));
        
        return errors;
    }
    
