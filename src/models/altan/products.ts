    private async validateSchemas(_product: AltanProduct, groups: Array<string> = ['POST']): Promise<any[]> {
        
        let errors: Array<any> = [];
        
        if(!isEmpty(_product)) {
            let product = new AltanProduct();
            product.name = _product.name;
            product.description = _product.description;
            product.typeValue = _product.typeValue;
            product.statusValue = _product.statusValue;
            product.offerId = _product.offerId;
            let productErrors = await validate(product, { groups, skipMissingProperties: true });
            errors = errors.concat(RemodelErrors(productErrors, 'product'));
        }
        
        return errors;
    }
    
